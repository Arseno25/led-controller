#define PALETTE_COUNT (sizeof(s_palettes) / sizeof(s_palettes[0]))

esp_err_t palette_manager_init(void) { return ESP_OK; }

size_t palette_manager_count(void) { return PALETTE_COUNT; }

const palette_t *palette_manager_get(uint8_t id)
{
    if (id >= PALETTE_COUNT) return NULL;
    return &s_palettes[id];
}

const palette_t *palette_manager_get_by_name(const char *name)
{
    if (!name) return NULL;
    for (size_t i = 0; i < PALETTE_COUNT; i++) {
        if (strcasecmp(s_palettes[i].name, name) == 0) return &s_palettes[i];
    }
    return NULL;
}

led_color_t palette_manager_sample(uint8_t id, uint8_t t)
{
    const palette_t *p = palette_manager_get(id);
    if (!p || p->color_count == 0) return (led_color_t){0, 0, 0, 0};
    if (p->color_count == 1) return p->colors[0];

    uint16_t scaled = (uint16_t)t * (p->color_count - 1);
    uint8_t  idx    = scaled / 255;
    uint8_t  frac   = scaled - idx * 255;
    if (idx >= p->color_count - 1) return p->colors[p->color_count - 1];

    led_color_t a = p->colors[idx];
    led_color_t b = p->colors[idx + 1];
    return (led_color_t){
        (uint8_t)(((uint16_t)a.r * (255 - frac) + (uint16_t)b.r * frac) / 255),
        (uint8_t)(((uint16_t)a.g * (255 - frac) + (uint16_t)b.g * frac) / 255),
        (uint8_t)(((uint16_t)a.b * (255 - frac) + (uint16_t)b.b * frac) / 255),
        (uint8_t)(((uint16_t)a.w * (255 - frac) + (uint16_t)b.w * frac) / 255),
    };
}
